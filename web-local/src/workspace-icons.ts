const workspaceIconLookup = new Map<string, WorkspaceIconOption>();

for (const iconOption of workspaceIconOptions) {
  workspaceIconLookup.set(iconOption.name, iconOption);
  for (const alias of iconOption.aliases ?? []) {
    workspaceIconLookup.set(alias, iconOption);
  }
}

export function workspaceIconOptionFor(iconName: string | null | undefined): WorkspaceIconOption {
  const normalizedIconName = iconName?.trim().toLowerCase();
  return normalizedIconName ? workspaceIconLookup.get(normalizedIconName) ?? workspaceIconOptions[0] : workspaceIconOptions[0];
}

export function filterWorkspaceIconOptions(query: string): WorkspaceIconOption[] {
  const tokens = query.trim().toLowerCase().split(/\s+/).filter(Boolean);
  if (!tokens.length) return workspaceIconOptions;
  return workspaceIconOptions
    .map((iconOption, index) => ({ iconOption, index, score: workspaceIconSearchScore(iconOption, tokens) }))
    .filter((result) => Number.isFinite(result.score))
    .sort((left, right) => left.score - right.score || left.index - right.index)
    .map((result) => result.iconOption);
}

function workspaceIconSearchScore(iconOption: WorkspaceIconOption, tokens: string[]): number {
  const values = [iconOption.name, iconOption.label, ...(iconOption.aliases ?? []), ...(iconOption.keywords ?? [])].map((value) => value.toLowerCase());
  const words = values.flatMap((value) => value.split(/[^a-z0-9]+/).filter(Boolean));
  let score = 0;
  for (const token of tokens) {
    const tokenScore = workspaceIconTokenScore(token, values, words);
    if (!Number.isFinite(tokenScore)) return Number.POSITIVE_INFINITY;
    score += tokenScore;
  }
  return score;
}

function workspaceIconTokenScore(token: string, values: string[], words: string[]): number {
  if (values.some((value) => value === token) || words.some((word) => word === token)) return 0;
  if (values.some((value) => value.startsWith(token)) || words.some((word) => word.startsWith(token))) return 1;
  if (values.some((value) => value.includes(token))) return 2;
  return Number.POSITIVE_INFINITY;
}
