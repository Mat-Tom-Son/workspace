export interface WorkspaceIconOption {
  name: string;
  label: string;
  Icon: FluentIcon;
  aliases?: string[];
  keywords?: string[];
}

function option(
  name: string,
  label: string,
  filledIcon: FluentIcon,
  regularIcon: FluentIcon,
  aliases: string[] = [],
  keywords: string[] = [],
): WorkspaceIconOption {
  return {
    name,
    label,
    Icon: bundleIcon(filledIcon, regularIcon),
    ...(aliases.length ? { aliases } : {}),
    ...(keywords.length ? { keywords } : {}),
  };
}

